namespace TheoryExamples
{
    public class Calculator
    {
        public int Add(int addend1, int addend2) => addend1 + addend2;
        public int Subtract(int minuend, int subtrahend) => minuend - subtrahend;
        public int Multiply(int multiplier, int multiplicand) => multiplier * multiplicand;
        public double Divide(int dividend, int divisor) => dividend / divisor;
    }
}